        $querry =  $this->createQueryBuilder('o')
        ->andWhere('o.user = :user')
        ->andWhere('o.state > 0')
        ->setParameter('user', $user)
        ->orderBy('o.id', 'DESC');

       return $querry->getQuery()->getResult()
